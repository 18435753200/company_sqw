package com.mall.po;
/**
 * B2C商品信息
 * @author DOUBLELL
 *
 */

public class ProductDetailB2c {
	private String b2cProductName;
	private String origin_Country;
	private String b2cMoneyUnitId;
	private String b2csupply;
	private String pid;

	public String getB2cProductName() {
		return b2cProductName;
	}

	public void setB2cProductName(String b2cProductName) {
		this.b2cProductName = b2cProductName;
	}

	public String getOrigin_Country() {
		return origin_Country;
	}

	public void setOrigin_Country(String origin_Country) {
		this.origin_Country = origin_Country;
	}

	public String getB2cMoneyUnitId() {
		return b2cMoneyUnitId;
	}

	public void setB2cMoneyUnitId(String b2cMoneyUnitId) {
		this.b2cMoneyUnitId = b2cMoneyUnitId;
	}

	public String getB2csupply() {
		return b2csupply;
	}

	public void setB2csupply(String b2csupply) {
		this.b2csupply = b2csupply;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}
	

}
